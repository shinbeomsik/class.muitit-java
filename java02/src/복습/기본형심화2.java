package 복습;
public class 기본형심화2 {
	public static void main(String[] args) {
		//정수형 변수 4가지
		// -128~127, 1바이트 byte
		// -3만~3만, 2바이트 short
		// -21억~21억, 4바이트 int
		//21억 이상, 8바이트 long
		
		byte age = 127;
		short days = 1000;
		int money = 100000;
		long bank = 2300000000L;
		//long 은 숫자뒤에 l,L (소문자,대문자)을 쓰기
		
		//살수형 변수 2가지
		
		//소수점  4자리까지 4바이트 float
		//소수점  4자리이상 8바이트 double
		float height = 170.4f;
		double weight = 43.33333333;
		//float 는 숫자뒤에 f,F(소문자,대문자)을쓰기
	
		
		//문자형 1가지  char
		
		//논리형 boolean
	}
}

